class DoubleAdd_abc{
public static void main(String abc[]){
double num1=Double.parseDouble(abc[0]);
double num2=Double.parseDouble(abc[1]);
double num3=Double.parseDouble(abc[2]);
System.out.println((num1+num2+num3));
System.out.println((num1-num2-num3));
System.out.println((num1*num2*num3));
System.out.println((num1%num2%num3));
System.out.println((num1/num2/num3));
}
}